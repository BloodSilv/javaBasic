package Udemy.Section5;

public class UpdateIncrementDecrement {
    public static void main(String[] args) {
        var a = 0;
        System.out.println(a);

        a = 5;
        System.out.println(a);

        a = a + 2; // a += 2
        System.out.println(a);

        a += 2;
        System.out.println(a);

        a += 1; //a++
        System.out.println(a);

        a++; //increment (--) decrement
        System.out.println(a);
    }
}
