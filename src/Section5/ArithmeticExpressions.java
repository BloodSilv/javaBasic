package Section5;

public class ArithmeticExpressions {
    public static void main(String[] args) {
        //integer
        {
            var a = 5;
            var b = 3;
            System.out.println(a + b);
            System.out.println(a * b);
            System.out.println(a - b);
            System.out.println(a / b);
            System.out.println(a % b);
        }
        {
            //double
            var a = 5.;
            var b = 3.;
            System.out.println(a + b);
            System.out.println(a * b);
            System.out.println(a - b);
            System.out.println(a / b);
            System.out.println(a % b);
        }
    }
}
